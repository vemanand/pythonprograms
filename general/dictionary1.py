'''
A dictionary is a data type similar to arrays, but works with keys and values instead of indexes.
Each value stored in a dictionary can be accessed using a key, which is any type of object (a string, a number, a list, etc.)
instead of using its index to address it. Dictionary is declared using curly brackets
'''

#Declare an empty dictionary and assign values
dict1 = {}
dict1["sub1"] = "English"
dict1["sub2"] = "Language2"
dict1["sub3"] = "Mathematics"
dict1["sub4"] = "Science"
dict1["Sub5"] = "Social"
print(dict1)

#Declare and assign values at once
dict2 = {"English":45,"Language2":47,"Mathematics":50}
print(dict2)
